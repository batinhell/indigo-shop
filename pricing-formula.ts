// ─── 4. ВАЛИДАЦИЯ ВХОДНЫХ ДАННЫХ ─────────────────────────────────────────────

export function isFabricAvailable(size: SizeKey, fabric: string): boolean {
  return fabric in PRICE_TABLE[size];
}

export function isQuantityValid(quantity: number): boolean {
  return Number.isInteger(quantity) && quantity >= 1;
}


// ─── 5. ОСНОВНАЯ ФУНКЦИЯ РАСЧЁТА ─────────────────────────────────────────────

export interface PriceInput {
  /** Размер флага */
  size: SizeKey;
  /** Ткань */
  fabric: string;
  /** Тираж (количество флагов) */
  quantity: number;
  /** Добавить бахрому? */
  hasFringe: boolean;
  /** Двусторонняя печать? */
  isDoubleSided: boolean;
  /** Заказать разработку макета? */
  needsDesign: boolean;
}

export interface PriceBreakdown {
  /** Базовая цена за 1 флаг из прайс-таблицы (руб.) */
  basePrice: number;
  /** Доплата за бахрому на 1 флаг (руб.) */
  fringeAddition: number;
  /** Доплата за двустороннюю печать на 1 флаг (руб.) */
  doubleSidedAddition: number;
  /** Итоговая цена за 1 флаг с учётом всех опций (руб.) */
  unitPrice: number;
  /** Стоимость тиража без разработки макета (руб.) */
  printTotal: number;
  /** Стоимость разработки макета (руб.), не зависит от тиража */
  designPrice: number;
  /** Итоговая сумма к оплате (руб.) */
  total: number;
  /** Срок производства (рабочих дней) */
  productionDays: number;
  /** Применённый тиражный диапазон */
  tier: Tier;
  /** Ошибка, если входные данные невалидны */
  error?: string;
}

/**
 * Рассчитывает стоимость заказа флага.
 *
 * @example
 * calculateFlagPrice({
 *   size: "90x135",
 *   fabric: "Флажная сетка",
 *   quantity: 10,
 *   hasFringe: true,
 *   isDoubleSided: false,
 *   needsDesign: true,
 * });
 * // → {
 * //     basePrice: 1300,       ← tier "5-10"
 * //     fringeAddition: 300,
 * //     doubleSidedAddition: 0,
 * //     unitPrice: 1600,       ← 1300 + 300 + 0
 * //     printTotal: 16000,     ← 1600 × 10
 * //     designPrice: 1500,
 * //     total: 17500,          ← 16000 + 1500
 * //     productionDays: 5,     ← 4 + 1
 * //     tier: "5-10",
 * //   }
 */
export function calculateFlagPrice(input: PriceInput): PriceBreakdown {
  const { size, fabric, quantity, hasFringe, isDoubleSided, needsDesign } = input;

  // Валидация
  if (!isQuantityValid(quantity)) {
    return {
      basePrice: 0, fringeAddition: 0, doubleSidedAddition: 0,
      unitPrice: 0, printTotal: 0, designPrice: 0, total: 0,
      productionDays: 0, tier: "1-4",
      error: `Некорректный тираж: ${quantity}. Должно быть целое число ≥ 1.`,
    };
  }

  if (!isFabricAvailable(size, fabric)) {
    return {
      basePrice: 0, fringeAddition: 0, doubleSidedAddition: 0,
      unitPrice: 0, printTotal: 0, designPrice: 0, total: 0,
      productionDays: 0, tier: "1-4",
      error: `Ткань "${fabric}" недоступна для размера ${size}.`,
    };
  }

  // Основной расчёт
  const tier              = getTier(quantity);
  const basePrice         = PRICE_TABLE[size][fabric][tier];
  const fringeAddition    = hasFringe    ? FRINGE_PRICE       : 0;
  const doubleSidedAddition = isDoubleSided ? DOUBLE_SIDED_PRICE : 0;
  const unitPrice         = basePrice + fringeAddition + doubleSidedAddition;
  const printTotal        = unitPrice * quantity;
  const designPrice       = needsDesign  ? DESIGN_PRICE        : 0;
  const total             = printTotal + designPrice;
  const productionDays    = BASE_PRODUCTION_DAYS + (needsDesign ? DESIGN_EXTRA_DAYS : 0);

  return {
    basePrice,
    fringeAddition,
    doubleSidedAddition,
    unitPrice,
    printTotal,
    designPrice,
    total,
    productionDays,
    tier,
  };
}



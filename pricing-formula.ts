// ─── 7. БИЗНЕС-ПРАВИЛА (важно для разработчика) ──────────────────────────────
//
//  ① Тираж "raopt" (≥100 шт): в калькуляторе используется та же цена,
//    что и для "20-100". Менеджер может изменить цену вручную при
//    оформлении оптового заказа.
//
//  ② Ткань "Атлас" существует только для размера 90×135.
//    Ткань "Атлас 210гр" существует только для размера 90×60.
//    При смене размера необходимо сбрасывать выбранную ткань,
//    если она недоступна для нового размера (isFabricAvailable()).
//
//  ③ Разработка макета (designPrice = 1 500 ₽) прибавляется ОДИН РАЗ,
//    не зависит от тиража. Это не "цена за флаг", а разовая услуга.
//
//  ④ Доплаты за бахрому и двустороннюю печать умножаются на тираж
//    (они считаются на каждый флаг отдельно).
//
//  ⑤ Срок производства: базовый = 4 рабочих дня.
//    Если заказана разработка макета — +1 рабочий день.
//    Итого: от 4 до 5 рабочих дней.
//    ⚠️ Не обе��ать срок менее 4 дней — это "точка невозврата".
//
//  ⑥ Оплата:
//    — Физические лица: СБП
//    — Юридические лица: счёт (безналичный расчёт)
//    Тип плательщика не влияет на расчёт стоимости.
//
//  ⑦ Цена на сайте должна ТОЧНО совпадать с ценой от менеджера.
//    При обновлении прайс-таблицы менять только объект PRICE_TABLE.
